
package sistemagerencial;


public class Peca {
    private String nome_item;
    private int qtd_item;
    private double valor_item;
    private double comp_item;
    private double larg_item;

   
    public String getNome_item() {
        return nome_item;
    }

    
    public void setNome_item(String nome_item) {
        this.nome_item = nome_item;
    }

    
    public int getQtd_item() {
        return qtd_item;
    }

    
    public void setQtd_item(int qtd_item) {
        this.qtd_item = qtd_item;
    }

    
    public double getValor_item() {
        return valor_item;
    }

    public void setValor_item(double valor_item) {
        this.valor_item = valor_item;
    }

    public double getComp_item() {
        return comp_item;
    }

    public void setComp_item(double comp_item) {
        this.comp_item = comp_item;
    }

    public double getLarg_item() {
        return larg_item;
    }

    public void setLarg_item(double larg_item) {
        this.larg_item = larg_item;
    }
    
    
            
    
}
